function showStore(d){

        let store = d;
        
        let store_div = document.getElementById("store_div")

        store_div.innerHTML = null;

        store.forEach(function(el){
            let value = el;
            let div = document.createElement("div");
            div.setAttribute("class","thumbnail")
            div.addEventListener("click", function(){
                
                if(localStorage.getItem("selectedItem")==null){
                localStorage.setItem("selectedItem",JSON.stringify(el))
                } else {
                    localStorage.removeItem("selectedItem");
                    localStorage.setItem("selectedItem",JSON.stringify(el))
                }
                window.location.href = "selectedItem.html";

            });
            

            let m_name = document.createElement("p");
            m_name.innerHTML = el.name;

            let m_id = document.createElement("p");
            m_id.innerHTML = el.id;

            let m_price = document.createElement("span");
            m_price.innerHTML = `₹${el.price}`;
            m_price.setAttribute("class", "price");

            let m_cprice = document.createElement("span");
            m_cprice.innerHTML = `₹${el.cprice}`;
            m_cprice.setAttribute("class", "cprice");

            let dis = document.createElement("span");
            dis.innerHTML = el.discount;
            dis.setAttribute("class", "discound");


            let m_title = document.createElement("p");
            m_title.innerHTML = el.title;
            m_title.setAttribute("class","title")

            let img = document.createElement("img");
            img.src = el.image;
            

            div.append(img, m_name, m_title, m_price, m_cprice, dis)
            

            store_div.append(div);

        })

}
